		<h2>Fleet Captain</h2>
		<div>
			<p>
				The Fleet Captain is the only position that is not elected by the membership;
				instead, the Fleet Captain is appointed by the executive board.
				The Fleet Captain is the behind-the-scenes person who makes sure that the rest of us can go sailing whenever we want.
				The Fleet Captain enforces boat checkout procedures, maintains all of the boats and equipment, and takes care of boat and trailer registration.
			</p>
