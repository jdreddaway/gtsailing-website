		<h2>Vice Commodore</h2>
		<div>
			<p>
				The Vice Commodore replaces the Commodore in the event that the Commodore temporarily cannot fulfill his/her duties.
				The Vice Commodore is in charge of sailing instruction and skipper certification.
				In addition, the VC should focus on achieving long-term goals for the club.
			</p>
			<p>
				The Vice Commodore does not have many specific jobs to do.
				Given this leeway, the VC can help out wherever he/she is needed.
				The Vice Commodore has the freedom to spearhead new club activities and programs.
			</p>
			<p>
					To contact the Vice Commodore, <a href="/contact/#vice_commodore">click here</a>.
			</p>
