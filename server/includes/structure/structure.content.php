		<h2>Secretary</h2>
		<div>
			<p>
				The Secretary is in charge of communication, both internally and externally.
				The Secretary records the minutes at the meetings and sends them out to the members.
				The Secretary should make sure that members are up-to-date with club activities.
			</p>
			<p>
				The Secretary needs to make sure the rest of the world knows about GT Sailing.
				The Secretary advertises the club to other Tech students and keeps in touch with alumni.
				This includes posting on social media, updating the website, writing articles and newsletters, chalking, flyering, etc.
			</p>
			<p>
				To contact the secretary, <a href="/contact/#secretary">click here</a>.
			</p>
