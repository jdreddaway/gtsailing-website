		<h2>Treasurer</h2>
		<div>
			<p>
				The treasury is one of the most important parts of the club&mdash;without money, we cannot maintain our boats and get new equipment, which is unsafe.
				The Treasurer manages all things money related, including collecting dues, issuing reimbursements, and purchasing new equipment.
				The Treasurer deals a lot with the Student Government Association (SGA), like submitting the club budget, submitting bills, and getting reimbursements from SOFO.
				Because the treasury is so important and complicated, the Treasurer may accept assistants who will help out and learn about the position.
			</p>
			<p>
				To contact the Treasurer, <a href="/contact/#treasurer">click here</a>.
			</p>
