	<div id="board" class="dark">
		<h2>Executive Board</h2>
		<div>
			<p>
				The executive board is elected by the membership to make decisions for the rest of the club.
				The executive board consists of the <a href="#commodore">Commodore</a>, <a href="#vice">Vice Commodore</a>,
				<a href="#treasurer">Treasurer</a>, <a href="#secretary">Secretary</a>, <a href="#race_captain">Race Team Captain</a>,
				<a href="#social_chair">Membership Chair</a>, and <a href="#fleet_captain">Fleet Captain</a>.
				Officers are supposed to attend at least 75% of the meetings.
				In order for any executive board decision to be legitimate, at least half of the officers must be present.
			</p>
			<p>
				Any member of the club may sit-in on executive meetings.
				If the membership disagrees with an executive board decision, then the membership may vote to reverse the decision, assuming that a quorum of 1/3 of total members is present.
				If the membership is not content with an officer, he/she may be removed from office by a 2/3 vote of the executive board and 2/3 vote of membership, or by 3/4 vote of regular members.
			</p>
		</div>
