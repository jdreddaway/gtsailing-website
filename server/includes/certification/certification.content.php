		<h2>Windsurf Certification</h2>
		<div>
			<p>
				There are three steps involved in becoming windsurf certified: a written test, an on-the-water test, and a swim test.
				After passing these three tests, your instructor will send a formal recommendation to the executive board.
				If exec approves, they will add windsurf qualification to your skipper card.
			</p>
			<p>
				For the swim test, you must:
				<ul>
					<li>Swim 25 yards unassisted (without a lifejacket)</li>
					<li>Tread water for 5 minutes unassisted (without a lifejacket)</li>
					<li>Put a lifejacket on over your head while in the water</li>
				</ul>
			</p>
			<p>
				As a windsurf certified skipper, you may:
				<ul>
					<li>use GTSC windsurf equipment without instructor supervision</li>
					<li>borrow GTSC windsurf equipment for use at locations other than LLSC following notification and approval of the executive board</li>
				</ul>
			</p>
