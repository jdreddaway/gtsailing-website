		<h2>420 Certification</h2>
		<div>
			<p>
				420 Certification is designed to be the first step for qualification in other fleets.
				There are two steps involved in becoming 420 qualified: a written test and an on-the-water test.
			</p>
			<p>
				The written test focuses on parts of the boat, right of way rules, and points of sail.
				After taking the written test, you may attempt the on-the-water test with a GTSC 420 instructor.
				The on-the-water test involves demonstrating the following skills:
				<ul>
					<li>
						<strong>Most important:</strong> Must be able to assess the conditions (wind, current, launch position, hazards) to determine whether you should go out based on your and your crew's skills and limitations
					</li>
					<li>Proper rigging (including knowledge of GTSC 420 equipment storage)</li>
					<li>Proper docking procedures</li>
					<li>Ability to skipper comfortably in moderate breeze (> 12 knots)</li>
					<li>Ability to confidently direct crew in their responsibilities</li>
					<li>Proper tacking and jibing</li>
					<li>Sailing at all points of sail</li>
					<li>Man overboard procedures</li>
					<li>Capsize recovery</li>
					<li>Proper de-rigging</li>
				</ul>
			</p>
			<p>
				After you pass the on-the-water test, your instructor will send a formal recommendation to the executive board.
				If exec approves, they will add 420 qualification to your GTSC Skipper card.
			</p>
			<p>
				As a 420 certified skipper, you may take up to 2 people out on the 420s and may attempt to become certified in other fleets.
			</p>
		</div>
