<h1>History</h1>
<p>
	The Georgia Tech Sailing Club was founded in 1961 by a group of students who discovered they had a common interest in sailing and racing.
	They wanted to encourage other students to take up their passion for sailing and began teaching everyone who wanted to learn.
	Over the years, the number of avid sailors at Georgia Tech has grown.
	We have accumulated an extensive <a href="/fleet">fleet</a> of different sailboats and watercraft.
	Throughout this expansion, we have maintained our emphasis on racing through participation in intercollegiate and local racing circuits.
	This sport is one of the most fun and exciting in the world, and we are proud to be an active part of the sailing community.
