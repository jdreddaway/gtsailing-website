	<div id="cert" class="dark">
		<h2><a href="/certification">Skipper Certification</a></h2>
		<div>
			<p>
				In order to protect our <a href="/fleet">equipment</a> and our members, we have developed a strict skipper certification process.
				Each fleet has a different process that is intended to ensure that only members who have demonstrated mastery of that boat may sail it.
				Once a member becomes skipper certified, they are allowed to skipper that kind of boat provided that a lakehost is present.
				For more information about the skipper certification process, <a href="/certification">click here</a>.
			</p>
			<p>
				Even if you are not skipper certified, you can still go sailing!
				However, most of the time you will have to crew for someone who is skipper certified.
				The only exception is when there is an instructor present, in which case you are allowed to skipper as long as he/she is supervising you.
			</p>
