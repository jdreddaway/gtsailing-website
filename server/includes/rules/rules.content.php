		<h2>Lakehosts</h2>
		<div>
			<p>
				We keep all our boats and equipment at <a target="_blank" href="http://www.llsc.com">Lake Lanier Sailing Club (LLSC)</a>.
				Only members of LLSC are allowed on the grounds, so GT Sailing holds 10 student memberships with them.
				Each year, our executive board appoints 10 students to receive our LLSC memberships.
				These 10 students are known as lakehosts.
			</p>
			<p>
				When appointing lakehosts, exec generally looks for students who are outgoing and actively involved in both sailing and maintaining our fleets.
				Lakehosts should be willing to take people up to the lake often and help them learn to sail.
				According to the <a href="/documents/Constitution_Bylaws.pdf">bylaws</a>, a lakehost must be:
			</p>
			<ul>
				<li>at least 420 or windsurf <a href="/certification">skipper certified</a>,</li>
				<li>a member of GTSC for at least two semesters,</li>
				<li>currently a member of GTSC,</li>
				<li>
					and either:
					<ul>
						<li>
							a student under 28 years old
						</li>
						<li>
							or a member of LLSC.
						</li>
					</ul>
				</li>
			</ul>
			<p>
				Nobody can be at LLSC unless they are a member or they are the guest of a member who is present.
				Since lakehosts are LLSC members, you can be at LLSC while they are also there.
				In addition, a lakehost must be present for anyone to use GTSC boats and equipment.
				The lakehost is in charge&mdash;while at LLSC, GTSC members must obey what they say.
			</p>
			<p>
				To become a lakehost, a student must be appointed by GTSC exec and must complete the <a href="/documents/LLSC_Member_Application.pdf" target="_blank">LLSC membership application form</a> and submit it to the current secretary of LLSC.
			</p>
		</div>
