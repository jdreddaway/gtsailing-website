		<h2>6 Club Rigged 420s</h2>
		<div>
			<p>
				The <a href="http://en.wikipedia.org/wiki/420_%28dinghy%29" target="_blank">Club 420</a> is a two-person dinghy that is about 14 feet long.
				The <a href="/raceteam">GT Sailing Race Team</a> uses these boats for practice for collegiate regattas.
				The <a href="http://www.llsc.com" target="_blank">Lake Lanier Sailing Club</a> Junior Program owns another 6 c420s.
				When we host the Georgia Tech regatta, LLSC graciously lets us borrow their boats so that we can have 12 boats racing at once.
			</p>
			<p>
				You must be skipper certified in order to skipper a 420.
				<a href="/certification#c420">Click here</a> to learn more about 420 certification.
			</p>
		</div>
	</div>
