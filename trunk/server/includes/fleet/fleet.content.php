<div id="j24s" class="fixed">
	<img class="background" src="/images/backgrounds/DSCF0972.png" />
	<div id="j24_d" class="light">
		<h2>2 J/24s</h2>
		<div>
			<p>
				A <a href="http://en.wikipedia.org/wiki/J/24" target="_blank">J/24</a> is a 24-foot keelboat that can take 2 to 6 people out on the water.
				They are great for both cruising and racing, and are also nice to have because they can take out so many people.
			</p>
			<p>
				Georgia Tech's J/24s are docked at LLSC's wet slips, which means that they are very easy to take out sailing.
				However, there must be either at least one fully J/24 certified skipper or at least two intermediate J/24 certified skippers on board.
				<a href="/certification#keelboat">Click here</a> to learn more about J/24 certification.
			</p>
		</div>
	</div>
</div>
<div id="lasers" class="fixed">
	<img class="background" src="/images/backgrounds/LaserDownwindPanorama4.png" />
	<div id="laser_d" class="light">
		<h2>5 Lasers</h2>
		<div>
			<p>
				A <a href="http://en.wikipedia.org/wiki/Laser_(dinghy)" target="_blank">Laser</a> is a single-person sailboat that is around 14 feet long.
				Lasers are extremely fun and challenging boats to sail.
				There is a large fleet of Lasers at LLSC and around the world, so races can get pretty competitive.
				In fact, Lasers are one of the boats that they sail in the Olympics!
			</p>
			<p>
				Georgia Tech owns 5 hulls, a few full rigs, a few radial rigs, and a couple 4.7 rigs.
				The <a href="/raceteam">race team</a> often competes in LLSC's frostbite series, which happens every Sunday from January through March.
				Although it can get pretty damn cold, the wind is excellent and there are a lot of other sailors to compete against.
			</p>
			<p>
				In order to sail a Laser, you must be Laser certified.
				<a href="/certification#laser">Click here</a> for more information about Laser certification.
			</p>
		</div>
	</div>
</div>
