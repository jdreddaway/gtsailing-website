		<h2>Lakehosts</h2>
		<div>
			<p>
				We keep all our boats and equipment at the Lake Lanier Sailing Club (LLSC).
				Only members of LLSC are allowed on the grounds so we hold 10 student memberships with LLSC.
				Each year, our executive board appoints 10 students to receive our LLSC memberships.
				These 10 students are known as lakehosts.
			</p>
			<p>
				A lakehost must be present in order for any of our members to be at LLSC, unless they are the guest of another LLSC member.
				In either case, a lakehost must be present in order for any member to use GTSC boats and equipment.
				The lakehost is in charge&mdash;while at LLSC, GTSC members must obey what they say.
			</p>
			<p>
				When appointing lakehosts, exec generally looks for students who are outgoing and actively involved in both sailing and maintaining our fleets.
				In addition, a lakehost should be:
			</p>
			<ul>
				<li>at least 420 or windsurf <a href="#qualification">skipper qualified</a></li>
				<li>a member of GTSC for at least one year</li>
				<li>plan on being a member of GTSC for at least one more year</li>
			</ul>
			<p>
				To become a lakehost, a student must be appointed by GTSC exec and must complete the <a href="documents/LLSC_Member_Application.pdf" target="_blank">LLSC membership application form</a> and submit it to the current secretary of LLSC.
			</p>
		</div>
	</div>
